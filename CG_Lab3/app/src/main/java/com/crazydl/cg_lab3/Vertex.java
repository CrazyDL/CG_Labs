package com.crazydl.cg_lab3;

class Vertex {
    private float x, y, z;

    public Vertex(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    float getX() {
        return x;
    }

    float getZ() {
        return z;
    }

    float getY() {
        return y;
    }
}
